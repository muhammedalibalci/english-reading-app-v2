export default function fontSizer(width) {
    if (width > 400) {
        return 20;
    } else if (width > 250) {
        return 18;
    } else {
        return 16;
    }
}

export  function booksImage(width){
    if (width > 400) {
        return 20;
    } else if (width > 250) {
        return 150;
    } else {
        return 16;
    }
}

